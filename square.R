#' Square a number
#'
#' takes x and squares it
#'
#' @author Mike Kozel
#'
#' @param x a numeric variable to be squared
#' @return the square of the input
#' @export


square <- function(x){
  return(x^2)
}

#' cube a number
#' @param X a number to be cubed
#' @return  the cube of the input
#' @export
cube <- function(x){
  return(x^3)
}
